local gray = require("number_sequences.gray")

print("FIRST N")
print("")

for gval, index in gray.FirstN(21) do
	print(gval, index)
end

print("")
print("FIRST N (CHANGE)")
print("")

local ii = 1

for gval, bit in gray.FirstN_Change(20) do
	print(gval, ii, bit)

	ii = ii + 1
end